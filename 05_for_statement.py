######################################
# for문 , in을 사용해서 반복하기
######################################
for looper in [1,2,3,4,5]:
    print(looper)

######################################
# for문 , range() 사용하기
######################################
for looper in range(0, 10):
    print(looper)

######################################
# for문 , list를 range()로 만들기
######################################
test_list = list(range(5))
for looper in test_list:
    print(looper)
